import base64
print ("""
###################################################
#        Base64 Encoder - Base64 Decoder          #
#        Coded By Murat YEŞİLTEPE                 #
#                                                 #
#        Encoder : e                              #
#        Decoder : d                              #
#        Çıkış   : ç                              #
###################################################
""")
while(True):
    ed = input("Karakter : ")
    if ed == 'e':
        encode = input("Base64 Encode : ")
        encoded = base64.b64encode(encode.encode('ascii'))
        print("Base64 Encoded : ",encoded)
    elif ed == 'd':
        decode = input("Base64 Decode : ")
        decoded = base64.b64decode(decode)
        print("Base64 Decoded : ",decoded)
    elif ed == 'ç':
         break
    else:
        print("Yanlış karakter girdiniz!")
